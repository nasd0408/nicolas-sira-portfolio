const html = document.documentElement;
const langButton = document.querySelector(".lang-toggle");
const langLabels = [...document.querySelectorAll("[data-lang]")];
const reducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
const finePointer = window.matchMedia("(pointer: fine)").matches;
const cursorOrb = document.querySelector(".cursor-orb");

function setLanguage(lang) {
  const dictionary = translations[lang] || translations.en;
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = dictionary[element.dataset.i18n];
    if (value) element.innerHTML = value;
  });
  html.lang = lang;
  langLabels.forEach((label) => label.classList.toggle("active", label.dataset.lang === lang));
  langButton.setAttribute("aria-pressed", String(lang === "es"));
  localStorage.setItem("nicolas-portfolio-language", lang);
}

const initialLanguage = localStorage.getItem("nicolas-portfolio-language")
  || (navigator.language.toLowerCase().startsWith("es") ? "es" : "en");
setLanguage(initialLanguage);

langButton.addEventListener("click", () => setLanguage(html.lang === "en" ? "es" : "en"));

const header = document.querySelector(".site-header");
const progress = document.querySelector(".scroll-progress span");
const horizontal = document.querySelector(".horizontal-sticky");
const track = document.querySelector(".horizontal-track");
const horizontalMeter = document.querySelector(".horizontal-meter span");
let framePending = false;

function updateScrollEffects() {
  const scrollTop = window.scrollY;
  const maxScroll = document.documentElement.scrollHeight - window.innerHeight;
  progress.style.transform = `scaleX(${maxScroll > 0 ? scrollTop / maxScroll : 0})`;
  header.classList.toggle("scrolled", scrollTop > 24);

  if (horizontal) {
    const rect = horizontal.getBoundingClientRect();
    const distance = horizontal.offsetHeight - window.innerHeight;
    const value = Math.max(0, Math.min(1, -rect.top / distance));
    const maxTranslate = Math.max(0, track.scrollWidth - window.innerWidth);
    track.style.transform = `translate3d(${-value * maxTranslate}px, 0, 0)`;
    horizontalMeter.style.transform = `scaleX(${value})`;
  }
  framePending = false;
}

window.addEventListener("scroll", () => {
  if (!framePending) {
    framePending = true;
    requestAnimationFrame(updateScrollEffects);
  }
}, { passive: true });
window.addEventListener("resize", updateScrollEffects);
updateScrollEffects();

if (!reducedMotion) {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: .12 });
  document.querySelectorAll(".reveal").forEach((element) => observer.observe(element));

  window.addEventListener("pointermove", (event) => {
    document.documentElement.style.setProperty("--mx", `${event.clientX}px`);
    document.documentElement.style.setProperty("--my", `${event.clientY}px`);
  }, { passive: true });
} else {
  document.querySelectorAll(".reveal").forEach((element) => element.classList.add("is-visible"));
}

if (finePointer && cursorOrb) {
  let targetX = window.innerWidth / 2;
  let targetY = window.innerHeight / 2;
  let currentX = targetX;
  let currentY = targetY;
  const interactiveSelector = "a, button, [role='button']";

  window.addEventListener("pointermove", (event) => {
    targetX = event.clientX;
    targetY = event.clientY;
    cursorOrb.style.opacity = "1";
    const hoveredElement = document.elementFromPoint(event.clientX, event.clientY);
    cursorOrb.classList.toggle("is-active", Boolean(hoveredElement?.closest(interactiveSelector)));
  }, { passive: true });

  document.documentElement.addEventListener("mouseleave", () => {
    cursorOrb.style.opacity = "0";
  });

  document.addEventListener("pointerover", (event) => {
    if (event.target.closest(interactiveSelector)) cursorOrb.classList.add("is-active");
  });
  document.addEventListener("pointerout", (event) => {
    const interactive = event.target.closest(interactiveSelector);
    if (interactive && !interactive.contains(event.relatedTarget)) {
      cursorOrb.classList.remove("is-active");
    }
  });

  const renderCursor = () => {
    currentX += (targetX - currentX) * .45;
    currentY += (targetY - currentY) * .45;
    cursorOrb.style.transform = `translate3d(${currentX}px, ${currentY}px, 0) translate(-50%, -50%)`;
    requestAnimationFrame(renderCursor);
  };
  renderCursor();
}

